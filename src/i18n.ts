// Automatic Language Detection & localstorage saving
const savedLanguage = localStorage.getItem("language");
const browserLanguage = typeof navigator !== "undefined" ? navigator.language.split("-")[0] : "ar";
const supportedLanguages = ["ar", "en", "de", "tr", "fr", "es"];

const defaultLanguage = savedLanguage || (supportedLanguages.includes(browserLanguage) ? browserLanguage : "ar");

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: defaultLanguage,
    fallbackLng: "ar",
    interpolation: {
      escapeValue: false // react already safes from xss
    }
  });

export default i18n;
